#include "../include/ft_printf.h"

int	*ft_unsigned(unsigned int nb, int *len)
{
	if (nb >= 10)
		ft_unsigned(nb / 10, len);
	*(len) += write (1, &"0123456789"[nb % 10], 1);
	return (len);
}
